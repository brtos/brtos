/**
* \struct OSTime
* Real time clock - shows the current hours, minutes and seconds or the uptime info
*/

typedef struct {

	uint8_t RTC_Second;         ///< Seconds of the clock
	uint8_t RTC_Minute;         ///< Minutes of the clock
	uint8_t RTC_Hour;	          ///< Hours of the clock

} OSTime;

