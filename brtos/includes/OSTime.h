struct _OSRTC
{
  uint16_t Year;
  uint8_t  Month;
  uint8_t  Day;
  uint8_t  Hour;
  uint8_t  Min;
  uint8_t  Sec;
};


typedef struct _OSRTC OS_RTC;
  




