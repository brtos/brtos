/////    OS Date Structure                             /////
////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////

/**
* \struct OSDate
* Operating System Date - Shows the current day, month and year ou the uptime info
*/

typedef struct {

	uint8_t       RTC_Day;          ///< Day of the date
	uint8_t       RTC_Month;        ///< Month of the date
  uint16_t RTC_Year;         ///< Year of the date
} OSDate; 

